#a lambda expression that adds 10 to the number passed into it as an argument
x = lambda a : a + 10
#prints the result of passing 5 into the above lambda expression
print(x(5))

#a lambda expression that multiplies the two numbers passed into it as arguments
y = lambda a, b : a * b
#prints the result of passing 5 and 6 into it
print(y(5, 6))
